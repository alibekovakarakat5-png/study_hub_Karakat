// ── Seed function ────────────────────────────────────────────────────────────

async function main() {
  console.log('Seeding StudyAbroadCountry data (12 countries)...')

  let created = 0
  let updated = 0

  for (const country of COUNTRIES) {
    const { countryCode, order, ...rest } = country

    const result = await prisma.studyAbroadCountry.upsert({
      where: { countryCode },
      update: {
        ...rest,
        order,
      },
      create: {
        countryCode,
        ...rest,
        order,
      },
    })

    // Check if it was an insert or update by comparing createdAt and updatedAt
    const isNew = result.createdAt.getTime() === result.updatedAt.getTime()
    if (isNew) {
      created++
      console.log(`  + ${country.flagEmoji} ${country.nameRu} (${countryCode}) — created`)
    } else {
      updated++
      console.log(`  ~ ${country.flagEmoji} ${country.nameRu} (${countryCode}) — updated`)
    }
  }

  console.log(`\nDone! Created: ${created}, Updated: ${updated}, Total: ${COUNTRIES.length}`)
}

main()
  .catch((e) => {
    console.error(e)
    process.exit(1)
  })
  .finally(() => prisma.$disconnect())
